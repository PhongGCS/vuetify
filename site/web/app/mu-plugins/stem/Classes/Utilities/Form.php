    public static function yearSelect($defaultValue, $startYear, $numberOfYears, $id = null, $name = null, $class = null, $selectedVal = null)
    {
        $years = [
            '-' => $defaultValue,
        ];

        for ($year = $startYear; $year <= $startYear + $numberOfYears; $year++) {
            $years[$year] = $year;
        }

        return self::buildSelect($id, $name, $class, $years, $selectedVal);
    }

